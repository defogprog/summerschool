/* while we use it in one place for particular setup just inline it */
static inline void iterateTLS(){
	uint16_t new_counter_period = TL_FLICK_DELAY;
	switch (++TL_state) {
		case TLS_FGL0:
		case TLS_FGH0:
		case TLS_FGL1:
		case TLS_FGH1:
		case TLS_FGL2:
		case TLS_FGH2:
		case TLS_FGL3:
		case TLS_FGH3:
			HAL_GPIO_TogglePin(TL_GPIO_Port, LED_G_Pin);
			break;
		case TLS_Y:
			HAL_GPIO_TogglePin(TL_GPIO_Port, LED_G_Pin | LED_Y_Pin);
			new_counter_period <<= 1;
			break;
		case TLS_R:
			HAL_GPIO_TogglePin(TL_GPIO_Port, LED_Y_Pin | LED_R_Pin);
			new_counter_period = TL_delayTime;
			break;
		case TLS_YR:
			HAL_GPIO_TogglePin(TL_GPIO_Port, LED_Y_Pin);
			new_counter_period <<= 1;
			break;
		default:
			HAL_GPIO_WritePin(TL_GPIO_Port, LED_Y_Pin | LED_R_Pin, GPIO_PIN_RESET);
			TL_state = 0;
		case TLS_G:
			HAL_GPIO_WritePin(TL_GPIO_Port, LED_G_Pin, GPIO_PIN_SET);
			HAL_TIM_Base_Stop_IT(&htim9);
			break;
	}
	__HAL_TIM_SET_AUTORELOAD(&htim9, new_counter_period);

}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
  if(htim == &htim9){
	  iterateTLS();
  }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if (GPIO_Pin == BUTTON_Pin && !TL_state){
		HAL_TIM_Base_Start_IT(&htim9);
	}
}

